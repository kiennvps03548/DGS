public class OutputItem {
    String config_seq, output_name, output_value;

    public String getConfig_seq() {
        return config_seq;
    }

    public void setConfig_seq(String config_seq) {
        this.config_seq = config_seq;
    }

    public String getOutput_name() {
        return output_name;
    }

    public void setOutput_name(String output_name) {
        this.output_name = output_name;
    }

    public String getOutput_value() {
        return output_value;
    }

    public void setOutput_value(String output_value) {
        this.output_value = output_value;
    }
}
